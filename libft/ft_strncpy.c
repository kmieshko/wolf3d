#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	size_t	i;

	i = 0;
	while (i < len && src[i] != '\0')
	{
		dst[i] = src[i];
		i++;
	}
	if (ft_strlen(src) < len)
	{
		len = len - i;
		while (len--)
		{
			dst[i] = 0;
			i++;
		}
	}
	return (dst);
}
